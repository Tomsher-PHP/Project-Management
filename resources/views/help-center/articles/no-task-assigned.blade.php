<p>If no task has been assigned, create a <strong>Task Request</strong>.</p>

<section>
    <h4 class="font-semibold text-bgray-900 dark:text-white">How to create a task request</h4>
    <p class="mt-2">Provide:</p>
    <ul class="mt-2 grid list-disc gap-1 pl-6 sm:grid-cols-2">
        <li>Task Name</li>
        <li>Estimated Time</li>
        <li>Priority</li>
        <li>Project</li>
        <li>Description</li>
        <li>Other required fields</li>
    </ul>
    <p class="mt-2">After submission, the request is sent to the assigned Reporter(s).</p>
</section>

<section>
    <h4 class="font-semibold text-bgray-900 dark:text-white">What happens after I submit a request?</h4>
    <p class="mt-2">A Reporter can approve or reject the request. Only approved task time contributes to project working hours; rejected task requests do not contribute to project time.</p>
</section>

